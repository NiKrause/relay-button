import { readFile, writeFile } from 'node:fs/promises'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const scriptDir = dirname(fileURLToPath(import.meta.url))
const packageRoot = dirname(scriptDir)
const packageJsonPath = join(packageRoot, 'package.json')
const outputPath = join(packageRoot, 'src', 'shared', 'package-version.ts')

const packageJson = JSON.parse(await readFile(packageJsonPath, 'utf8'))
const version = typeof packageJson.version === 'string' ? packageJson.version.trim() : ''

if (!version) {
  throw new Error(`Unable to derive package version from ${packageJsonPath}`)
}

const source = [
  '// Generated by packages/ui/scripts/write-package-version.mjs',
  `export const UI_PACKAGE_VERSION = ${JSON.stringify(version)}`,
  '',
].join('\n')

await writeFile(outputPath, source)
